                    <h2>1981</h2>
                    <p>Fondation du Front commun québécois pour les espaces verts et les sites naturels.</p>
                  </div>
        
                  <div class="boite1980 no2">
                    <h2>1982</h2>
                    <p>Participation aux audiences du BAPE sur la foresterie, lesquelles aboutissent à l’adoption de la Loi sur les forêts.</p>
                  </div>
        
                  <div class="boite1980 no3">
                    <h2>1983</h2>
                    <p>Le Front commun québécois pour les espaces verts et les sites naturels devient l’Union québécoise pour la conservation de la nature (UQCN).</p>
                  </div>
        
                  <div class="boite1980 no4">
                    <h2>1984</h2>
                    <p>Parution du premier numéro de Franc-Nord, magazine qui informe sur le patrimoine naturel et humain des paysages québécois.</p>
                    </div>
                  <div class="boite1980 no5">
                    <h2>1986</h2>
                    <p>Adoption de la Loi sur les forêts qui découle du travail de concertation de l’UQCN et d’autres organismes.</p>
                  </div>
        
                  <div class="boite1980 no6">
                    <h2>1987</h2>
                    <p>L’UQCN dévoile les premiers gagnants du concours de photos La nature du Québec en images du magazine Franc-Nord.</p>
                  </div>
        
                  <div class="boite1980 no7">
                    <h2>1988</h2>
                    <p>Sous la direction de l’UQCN, douze organismes riverains fondent Stratégies Saint-Laurent pour veiller à la gestion intégrée du fleuve.</p>
                    </div>
        
                  <div class="boite1980 no8">
                    <h2>1989</h2>
                    <p>Harvey Mead, président-fondateur de l’UQCN, devient sous-ministre adjoint au développement durable et à la conservation au ministère de l’Environnement, à la suite de revendications de plusieurs organismes environnementaux.</p>
