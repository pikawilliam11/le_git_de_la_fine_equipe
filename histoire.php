              <h2>Quatre décennies au coeur <br> de l'enjeu environnemental</h2>
              <div class="boiteOuverture">
                <p>En 1981, Harvey Mead et un petit groupe d’impliqués dans des organismes locaux créent le Front commun québécois pour les espaces verts (qui deviendra l’UQCN, puis Nature Québec). Il est formé en réaction à plusieurs projets qui menacent les milieux naturels aux battures de Beauport, au Kamouraska et autour du lac Saint-Pierre.</p>
              </div>  
                <div class="boiteOuverture">
                <p>Au fil des années, Nature Québec acquiert une notoriété publique et devient la première ONG québécoise d’envergure dédiée à la conservation des milieux naturels et de la biodiversité. </p>
              </div> 
                
              <div class="boiteOuverture">
                <p>L’organisme publiera notamment le premier magazine québécois consacré à la nature et l’environnement Franc-Nord / Franc-vert et se positionnera publiquement dans plusieurs dossiers qui contribueront à bâtir sa crédibilité.</p>
              </div> 
        
                <div class="boiteOuverture">
                <p>Aujourd’hui, Nature Québec compte plus de 15 employés permanents et collabore avec des dizaines d’experts pour la publication de nombreux rapports et mémoires. Nature Québec donne aussi plusieurs activités de sensibilisation/conférences et participe à la réalisation des nombreux projets sur le terrain en ville comme en région.</p>
              </div> 
